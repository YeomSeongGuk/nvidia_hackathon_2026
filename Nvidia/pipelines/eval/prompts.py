STAGE_1_1_JUDGE_SYSTEM = """detailed thinking off
You evaluate the SYNTHETIC FASHION REVIEW output of a Data Designer stage.
Each record contains: a persona profile, a product_title, product_attributes
(color / style / size), a raw_text review, and a star rating. The review
was generated by an LLM from the persona + a seed review; you check whether
the generator actually followed the spec.

Rubric
======

1) is_fashion (bool)
   TRUE when both product_title AND raw_text describe a fashion item
   (clothing, footwear, bags, accessories, jewelry-as-fashion). FALSE when
   the record is about cosmetics, food, electronics, furniture / home goods
   (e.g. "수납 정리함", "신발 정리함"), car seats, toys, etc.

2) title_within_spec (bool)
   TRUE when product_title is roughly 15-30 characters AND is a single
   clean line. FALSE when it contains newlines, the character '→', the
   literal words '글자' or '다시 조정', or explicit count markers like
   "(14자)". The reference prompt demanded exactly "브랜드명 + 상품종류 +
   특징" within 15-30 chars on ONE LINE.

3) title_format_ok (bool)
   TRUE when title has a recognisable brand name (even a plausible Korean
   retail brand like 유니클로 / 자라 / 에잇세컨즈 / 탑텐 / 스파오 / 무신사)
   followed by a product category and a distinguishing feature. FALSE when
   the title is only a product type ("티셔츠") or only a feature ("부드러운
   착용감") without brand / category structure.

4) title_has_reasoning_leak (bool)
   TRUE when product_title contains the model's self-verification chain
   (character-counting, "다시 조정", arrows, "조건 미달", "글자 수 세기").
   This catches the Nemotron-Super reasoning-leak bug. FALSE otherwise.

5) raw_text_within_spec (bool)
   TRUE when raw_text is roughly 80-200 characters and is coherent Korean.
   FALSE if <30 chars, >400 chars, or contains garbled / non-Korean text.

6) raw_text_naturalness (int 1-5)
   How natural the Korean writing sounds.
     5 = indistinguishable from a real Korean shopping review
     4 = slightly formal / LLM-ish but natural
     3 = noticeably templated but readable
     2 = awkward Korean with grammar errors
     1 = broken / translationese / unreadable

7) persona_reflection (int 1-5)
   How deeply the review reflects the persona's hobbies / occupation /
   character.
     5 = explicit references to hobbies/occupation/location shaped the review
     4 = implicit but clear persona cues
     3 = only age/sex hints visible
     2 = persona barely influenced wording
     1 = persona ignored completely

8) has_tpo_signal (bool)
   TRUE when raw_text mentions at least one occasion, setting, season or
   style cue (출근용, 하객, 데이트, 등산, 여행, 계절, 데일리 etc.).

9) attributes_grounded (list[AttrGrounded])
   For EACH of color / style / size in product_attributes, emit:
     - key: "color" | "style" | "size"
     - value: echoed value
     - mentioned_in_text: TRUE when the value (or an obvious synonym) appears in raw_text
     - plausible_for_product: TRUE when the value is plausible for the
       product_title (e.g. size="13개월" on a coat = implausible; size="M"
       on a t-shirt = plausible)

10) rating_sentiment_consistent (bool)
    TRUE when review tone matches the star rating:
      5 stars → positive, 4 → mostly positive, 1-2 → negative,
      3 → neutral / mixed. FALSE when the review contradicts the rating.

11) failure_modes (list[str]) — zero or more of:
    "title_reasoning_leak"
    "title_length_violation"
    "title_format_violation"
    "attr_ungrounded"
    "attr_mono_value"
    "non_fashion_item"
    "persona_drift"
    "missing_tpo"
    "length_violation"
    "rating_sentiment_mismatch"
    "language_issue"

12) reasoning (string)
    1-3 sentence justification covering the most important verdict(s).

Return ONLY a single JSON object. No prose, no markdown fences, no extra
text. Example shape:
{
  "is_fashion": true,
  "title_within_spec": false,
  "title_format_ok": false,
  "title_has_reasoning_leak": true,
  "raw_text_within_spec": true,
  "raw_text_naturalness": 4,
  "persona_reflection": 4,
  "has_tpo_signal": true,
  "attributes_grounded": [
    {"key":"color","value":"화이트","mentioned_in_text":true,"plausible_for_product":true},
    {"key":"style","value":"캐주얼","mentioned_in_text":false,"plausible_for_product":true},
    {"key":"size","value":"M","mentioned_in_text":true,"plausible_for_product":true}
  ],
  "rating_sentiment_consistent": true,
  "failure_modes": ["title_reasoning_leak"],
  "reasoning": "Title contains arrow + '글자 수 세기' self-verification trace. Other fields are OK."
}
"""


