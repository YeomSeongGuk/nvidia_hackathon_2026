"""Rubric prompts for LLM-as-Judge evaluation (one block per stage).

Design choices:

- System prompts start with the literal string `detailed thinking off` so
  NVIDIA Nemotron reasoning models skip chain-of-thought and emit JSON
  directly (the NIM gateway returns content=None otherwise).

- Rubrics are intentionally English; the judge (Nemotron-Ultra / Llama-3.3
  / Qwen3 / GLM-5 / DeepSeek-V3 / K-EXAONE) is multilingual and we want a
  single consistent rubric regardless of whether the evaluated text is
  Korean, English, or mixed.

- The JSON schema is restated inside the system prompt as an anchor; the
  Pydantic schema in `schemas.py` is the authoritative contract and will
  reject any deviation.
"""

